// ============================================================
// 文件作用：通用格式化工具函数（纯 JS，可在客户端和服务端使用）
// 功能对应：阅读量等数据的展示格式化
// ============================================================

/**
 * 格式化阅读量显示（如 2900 → "2.9k"）
 * @param {number} views - 阅读数量
 * @returns {string} 格式化后的字符串
 */
export function formatViews(views) {
  if (views >= 1000) {
    return (views / 1000).toFixed(1) + "k";
  }
  return String(views);
}
